package br.com.lbenaducci;

public class Calculadora {

    public long somar(int i, int i1) {

        return i + i1;
    }

    public long subtrair(int i, int i1) {
        return i - i1;
    }

    public long multiplicar(int i, int i1) {
        return (long) i * i1;
    }

    public double dividir(int i, int i1) {
        return (double) i / i1;
    }


}


